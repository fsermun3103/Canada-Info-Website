document.addEventListener("DOMContentLoaded", () => {
  const navbar = document.getElementById("navbar");
  const burger = document.querySelector(".burger");
  const menu = document.querySelector(".menu");
  const scrollTopBtn = document.getElementById("scrollTopBtn");
  const mapElement = document.getElementById("map");
  const cultureSection = document.querySelector(".culture");
  const bestTimeSection = document.querySelector(".best-time");

  if (navbar) {
    window.addEventListener("scroll", () => {
      if (window.scrollY > 200) {
        navbar.classList.add("scrolled");
      } else {
        navbar.classList.remove("scrolled");
      }
    });
  }

  if (burger && menu) {
    burger.addEventListener("click", () => {
      if (menu.classList.contains("open")) {
        burger.classList.remove("open");

        setTimeout(() => {
          menu.classList.remove("open");
        }, 300);

        menu.classList.add("closing");

        setTimeout(() => {
          menu.classList.remove("closing");
        }, 300);
      } else {
        burger.classList.add("open");
        menu.classList.add("open");
      }
    });
  }

  if (cultureSection) {
    const items = document.querySelectorAll(".item");
    const col2Title = document.querySelector(".col-2 h1");
    const col2Paragraphs = document.querySelectorAll(".col-2 p");
    const col3Image = document.querySelector(".col-3 img");
    const col3Caption = document.querySelector(".col-3 p");

    const eventImage = document.getElementById("event-image");
    const eventTitle = document.getElementById("event-title");
    const eventDate = document.getElementById("event-date");
    const eventText = document.getElementById("event-text");
    const eventCounter = document.getElementById("event-counter");
    const arrowLeft = document.getElementById("arrow-left");
    const arrowRight = document.getElementById("arrow-right");

    let currentCategory = "history";
    let currentEventIndex = 0;

    function renderEvent() {
      const events = data[currentCategory].events;
      const event = events[currentEventIndex];

      eventImage.src = event.img;
      eventImage.alt = event.title;
      eventTitle.innerHTML = `<strong>${event.title}</strong>`;
      eventDate.textContent = event.date;
      eventText.innerHTML = event.text;
      eventCounter.textContent = `${currentEventIndex + 1} / ${events.length}`;
    }

    function renderCategory(category) {
      const categoryData = data[category];

      col2Title.innerHTML = categoryData.title;
      col2Paragraphs[0].innerHTML = categoryData.text1;
      col2Paragraphs[1].innerHTML = categoryData.text2;

      col3Image.src = categoryData.img;
      col3Image.alt = categoryData.title;
      col3Caption.innerHTML = categoryData.caption;

      currentCategory = category;
      currentEventIndex = 0;

      renderEvent();
    }

    items.forEach((item) => {
      item.addEventListener("click", () => {
        items.forEach((el) => el.classList.remove("active"));
        item.classList.add("active");
        renderCategory(item.dataset.category);
      });
    });

    if (arrowRight) {
      arrowRight.addEventListener("click", () => {
        const events = data[currentCategory].events;
        currentEventIndex = (currentEventIndex + 1) % events.length;
        renderEvent();
      });
    }

    if (arrowLeft) {
      arrowLeft.addEventListener("click", () => {
        const events = data[currentCategory].events;
        currentEventIndex = (currentEventIndex - 1 + events.length) % events.length;
        renderEvent();
      });
    }

    renderCategory(currentCategory);
  }

  if (bestTimeSection) {
    const seasonTabs = document.querySelectorAll(".season-tab");
    const seasonImage = document.getElementById("season-image");
    const seasonTag = document.getElementById("season-tag");
    const seasonTitle = document.getElementById("season-title");
    const seasonText = document.getElementById("season-text");
    const seasonHighlights = document.getElementById("season-highlights");

    function renderSeason(season) {
      const seasonData = seasons[season];

      seasonImage.src = seasonData.img;
      seasonImage.alt = seasonData.alt;
      seasonTag.textContent = seasonData.tag;
      seasonTitle.textContent = seasonData.title;
      seasonText.innerHTML = seasonData.text;

      seasonHighlights.innerHTML = seasonData.highlights.map(item => `
        <article class="highlight-card">
          <h4>${item.title}</h4>
          <p>${item.text}</p>
        </article>
      `).join("");
    }

    seasonTabs.forEach((tab) => {
      tab.addEventListener("click", () => {
        seasonTabs.forEach((btn) => btn.classList.remove("active"));
        tab.classList.add("active");
        renderSeason(tab.dataset.season);
      });
    });

    renderSeason("spring");
  }

  if (scrollTopBtn) {
    window.addEventListener("scroll", () => {
      if (window.scrollY > 400) {
        scrollTopBtn.classList.add("show");
      } else {
        scrollTopBtn.classList.remove("show");
      }
    });

    scrollTopBtn.addEventListener("click", () => {
      window.scrollTo({
        top: 0,
        behavior: "smooth"
      });
    });
  }

  if (mapElement && typeof L !== "undefined") {
    const map = L.map("map").setView([56.1304, -106.3468], 4);

    const customIcon = L.icon({
      iconUrl: "assets/img/leaf.svg",
      iconSize: [32, 32],
      iconAnchor: [16, 31],
      popupAnchor: [0, -32]
    });

    L.tileLayer("https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png", {
      attribution: "&copy; OpenStreetMap contributors"
    }).addTo(map);

    fetch("landmarks.json")
      .then(res => res.json())
      .then(points => {
        points.forEach(point => {
          L.marker([point.lat, point.lng], { icon: customIcon })
            .addTo(map)
            .bindPopup(`
              <strong>${point.name}</strong><br>
              ${point.description}
            `);
        });
      });
  }
});